class AppUser {
  final String id;
  final String email;
  final String summonerName;
  final String tagLine;
  final String serverCode;
  final String name;
  final String surname;

  const AppUser({
    required this.id,
    required this.email,
    required this.summonerName,
    required this.tagLine,
    required this.serverCode,
    required this.name,
    required this.surname,
  });
}
